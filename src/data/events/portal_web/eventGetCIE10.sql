SELECT 
		CIE10 = SS_GE_DIAGNOSTICO.CODIGODIAGNOSTICO, 
		DIAGNOSTICO = RTRIM(SS_GE_DIAGNOSTICO.NOMBRE), 
		indicadorSexo = (CASE WHEN SS_GE_DIAGNOSTICO.SEXO = 'F' THEN 'Femenino' WHEN SS_GE_DIAGNOSTICO.SEXO = 'M' THEN 'Masculino' ELSE '' END),
		indicadorCronico = (CASE WHEN ISNULL(SS_GE_DIAGNOSTICO.INDICADORCRONICO,0) = 1 THEN 'SI' ELSE 'NO' END)
 FROM SS_GE_DIAGNOSTICO
WHERE SS_GE_DIAGNOSTICO.ESTADO = 2 
  AND LEN(SS_GE_DIAGNOSTICO.CODIGODIAGNOSTICO) > 4
ORDER BY SS_GE_DIAGNOSTICO.CODIGODIAGNOSTICO